package javaprograms;

public class DefaultValueGlobalVar {
	 byte a;
	 short b;
	 int c;
	 long d;
	 float e;
	 double f;
	 char g;
	 boolean h;
	 String i;
	
public static void main(String[] args)
{
	DefaultValueGlobalVar gv = new DefaultValueGlobalVar();
	System.out.println(gv.a);
	System.out.println(gv.b);
	System.out.println(gv.c);
	System.out.println(gv.d);
	System.out.println(gv.e);
	System.out.println(gv.f);
	System.out.println(gv.g);
	System.out.println(gv.h);
	System.out.println(gv.i);
	
	}

}
